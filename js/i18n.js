// Current language
let currentLang = localStorage.getItem('lpk_lang') || 'id';

/**
 * Get translation by key
 */
export function t(key) {
  return translations[currentLang]?.[key] || translations['id']?.[key] || key;
}

/**
 * Get current language
 */
export function getLang() {
  return currentLang;
}

/**
 * Set language and apply to all [data-i18n] elements
 */
export function setLang(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem('lpk_lang', lang);
  document.documentElement.lang = lang;

  // Update all elements with data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (el.hasAttribute('data-i18n-html')) {
      el.innerHTML = val;
    } else {
      el.textContent = val;
    }
  });

  // Update lang switcher active state
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

/**
 * Init language switcher
 */
export function initI18n() {
  // Apply saved language on page load
  setLang(currentLang);

  // Bind click events on lang switcher buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      setLang(btn.dataset.lang);
    });
  });
}


// Allow dynamically injecting translations from Firebase
export function updateTranslation(key, idText, jaText) {
  if (translations.id && idText) translations.id[key] = idText;
  if (translations.ja && jaText) translations.ja[key] = jaText;
}
